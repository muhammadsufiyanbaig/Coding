// Relational/ comperision Operators

#include <stdio.h>

int main() {
    int a = 5, b = 10;

    printf("0 represent false and 1 represent true\n");
    printf("%d > %d is %d\n", a, b, a > b);
    printf("%d < %d is %d\n", a, b, a < b);
    printf("%d <= %d is %d\n", a, b, a <= b);
    printf("%d >= %d is %d\n", a, b, a >= b);
    printf("%d == %d is %d\n", a, b, a == b);
    printf("%d != %d is %d\n", a, b, a != b);
    return 0;
}
