//vaiable and data types

#include <stdio.h>

int main() {
    int num1 = 10;
    char letter = 'A';
    float floatNum = 3.14;
    long int bigNum = 1234567890;
    char str[] = "Hello";

    printf("Integer: %d\n", num1);
    printf("Character: %c\n", letter);
    printf("Float: %f\n", floatNum);
    printf("Long Integer: %ld\n", bigNum);
    printf("String: %s\n", str);

    return 0;
}
